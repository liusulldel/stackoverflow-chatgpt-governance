def main() -> None:
    PROCESSED.mkdir(parents=True, exist_ok=True)
    PAPER.mkdir(parents=True, exist_ok=True)

    metadata, question_ids = load_question_metadata()
    answer_to_question = load_answer_mapping(question_ids)
    compiled = compile_patterns()
    hits = stream_post_hits(question_ids, answer_to_question, metadata, compiled)
    hits = stream_comment_hits(hits, question_ids, answer_to_question, metadata, compiled)
    hit_df = finalize_hits(hits)
    summary = build_outputs(hit_df, len(question_ids))
    write_memo(summary)
    print(json.dumps(summary, indent=2))


if __name__ == "__main__":
    main()
